/*-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-
    ダイスクラス
  -=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-*/
phina.define("Dices",
  {
    /*---=---=---=---=---=---=---=---=---=---=---=---=---=---=---=---
        コンストラクタ
      ---=---=---=---=---=---=---=---=---=---=---=---=---=---=---=---*/
    init: function (dices, bonus)
    {
      this.ダイス = dices; // [ [数, ダイスタイプ] ]
      this.固定値 = bonus;

      this.roll = function ()
      {
        let nums = new Array();
        for (let i = 0; i < this.ダイス.length; i++)
        {
          for (let j = 0; j < this.ダイス[i][0]; j++)
          {
            nums.push(Math.floor(Math.random() * this.ダイス[i][1]) + 1);
          }
        }
        if (this.固定値 > 0) nums.push(this.固定値);
        return nums;
      }
    }
    /*---=---=---=---=---=---=---=---=---=---=---=---=---=---=---=---*/
  });
/*-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-*/

